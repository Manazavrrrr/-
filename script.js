function waLink(serviceTitle) {
  const text = `Здравствуйте! Меня интересует услуга: ${serviceTitle}. Подскажите по стоимости и срокам.`;
  return `https://wa.me/${WA_PHONE}?text=${encodeURIComponent(text)}`;
}

